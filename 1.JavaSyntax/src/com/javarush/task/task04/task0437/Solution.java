package com.javarush.task.task04.task0437;

/* 
Треугольник из восьмерок
*/

public class Solution {
    public static void main(String[] args) {
        for (int n = 1; n<11; n++)
        {
            for (int x = 0; x<n; x++)
            {
                System.out.print(8);
            }
            System.out.println();
        }



    }
}
